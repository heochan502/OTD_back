package com.otd.onetoday_back.weather.util;

public class LatXLngY {
    public double lat;
    public double lng;
    public int nx;
    public int ny;

    @Override
    public String toString() {
        return "LatXLngY{" +
                "lat=" + lat +
                ", lng=" + lng +
                ", nx=" + nx +
                ", ny=" + ny +
                '}';
    }
}
